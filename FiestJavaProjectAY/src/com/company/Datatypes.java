package com.company;
import java.util.Scanner;
public class Datatypes {
    public static void main(String args[]){
        byte x = 5;
        int y = 6;
        short z = 8;
        int a = y + z;
        float b = 6.54f + x;
        System.out.println(b);
        // Increment and Decrement operators
        int i = 56;
        System.out.println(i++);
        System.out.println(i);
        System.out.println(++i);
        System.out.println(i);
        char ch = 'a';
        System.out.println(++ch);
    }
}
