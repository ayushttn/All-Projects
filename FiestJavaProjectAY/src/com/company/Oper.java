package com.company;
import java.util.Scanner;
public class Oper {
    public static void main(String args[]){
        // Assignment operators
        int b = 9;
        b *= 3;
        System.out.println(b);
        System.out.println(6==6); // comparison operators
        System.out.println(64>5 && 64>77); // logical operator
        System.out.println(64>5 || 64>77); // bitwise operator
    }
}
